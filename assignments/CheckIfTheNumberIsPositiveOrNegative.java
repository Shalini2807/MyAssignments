public class CheckIfTheNumberIsPositiveOrNegative {
public static void main(String[] args) {
	int number=35;  //number is Initialized(int)
	if(number>=25)
		System.out.println("The number is positive");  //If a number is positive, print "The number is positive"
	else if(number<25)
		System.out.println("The number is negative");   //If a number is negative, print "The number is negative" 
	else
		System.out.println("The number is neither positive nor negative");  //If it nether negative nor positive, print "The number is neither positive nor negative"
		
}

}
